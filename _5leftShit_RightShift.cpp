#include <iostream>
using namespace std;

int main() {
    cout << "right shift of (17 >> 1) is: " << (17 >> 1) << endl;
    cout << "right shift of (17 >> 2) is: " << (17 >> 2) << endl;
    cout << "left shift of (19 << 1) is: " << (19 << 1) << endl;
    cout << "left shift of (21 << 2) is: " << (21 << 2) << endl;
 }